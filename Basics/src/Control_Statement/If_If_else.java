package Control_Statement;

public class If_If_else {

	public static void main(String[] args) {
		int flag = 1;
		if(flag == 1)
		{
			System.out.println("System Start");
		}
		else{
			System.out.println("System Stop");
			
		}
		flag = 0;
		if(flag == 1)
		{
			System.out.println("System Start");
		}
		else{
			System.out.println("System Stop");
			
		}

	}

}
