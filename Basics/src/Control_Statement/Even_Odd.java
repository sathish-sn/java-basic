package Control_Statement;

public class Even_Odd {

	public static void main(String[] args) {
		int num = 20;
		int rem = num%2;
		switch(rem)
		{
		case 1:
			System.out.println("entered number is a odd number ");
			break;
		case 0:
			System.out.println("entered number is a even number ");
			//break;
			
		}
	}

}
